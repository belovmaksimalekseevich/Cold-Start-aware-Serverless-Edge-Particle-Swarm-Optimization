"""
Конфигурация модели и алгоритма. Один источник правды для всех параметров.

Все параметры собраны в один frozen-dataclass Config. Это удобно для тестов
(не нужны файлы), для воспроизводимости (всё засеяно через seed) и для
экспериментов (вариант создаётся через dataclasses.replace).

Диапазоны задач/узлов подобраны на spike Фазы 0 так, чтобы режим был здоровым:
базовая задержка единицы–десятки мс, completion 0.8–0.98, T_cold сопоставим
с задержкой очереди (не доминирует над всем).
"""
from dataclasses import dataclass, replace


@dataclass(frozen=True)
class Config:
    # --- размеры системы ---
    N: int = 50            # число устройств-источников
    M: int = 8             # число пограничных узлов (MEC)
    G: int = 12            # число типов функций (FaaS)

    # --- параметры PSO ---
    K: int = 40            # размер роя (частиц)
    Q: int = 30            # число итераций
    omega: float = 0.7     # коэффициент инерции (если фиксированный)
    c1: float = 1.5        # когнитивный коэффициент
    c2: float = 1.5        # социальный коэффициент
    omega_start: float = 0.9   # адаптивное расписание ω: начало
    omega_end: float = 0.4     # адаптивное расписание ω: конец
    adaptive: bool = True      # включить адаптивные расписания
    warm_start: bool = True    # переносить g_best между тайм-слотами
    restart_patience: int = 10 # итераций без улучшения до перезапуска части роя

    # --- веса целевой функции (F = α·D̄ + β·ΔL + γ·E + δ_cs·CSR) ---
    alpha: float = 0.6     # средняя задержка
    beta: float = 0.3      # дисбаланс нагрузки
    gamma: float = 0.1     # энергопотребление
    delta_cs: float = 0.0  # cold-start rate (0 = cold-NAIVE; >0 = cold-AWARE)

    # --- serverless-среда ---
    tau: float = 0.1       # длительность тайм-слота, с
    keep_alive: int = 5    # сколько слотов функция остаётся тёплой после вызова
    w_max: int = 4         # сколько тёплых функций помещается на узле (память)
    T_cold: float = 0.05   # штраф холодного старта, с

    # --- генерация задач ---
    zipf_a: float = 1.3    # перекос популярности типов функций (реалистичный skew)
    D_min: float = 10e3;   D_max: float = 100e3   # объём данных, байт
    C_min: float = 1e7;    C_max: float = 1e8     # вычислительная сложность, циклов
    R_min: float = 20e6;   R_max: float = 100e6   # скорость канала, бит/с
    Tmax_min: float = 0.05; Tmax_max: float = 0.10  # дедлайн задачи, с

    # --- узлы ---
    F_edge_min: float = 3e9; F_edge_max: float = 8e9   # производительность MEC, цикл/с
    F_loc_min: float = 0.1e9; F_loc_max: float = 0.5e9 # производительность устройств

    # --- прочее ---
    energy_coef: float = 0.05  # коэффициент энергии (усл.)
    seed: int = 0

    def variant(self, **kwargs) -> "Config":
        """Создать вариант конфига с изменёнными полями (не мутирует исходный)."""
        return replace(self, **kwargs)


# Пресеты
DEFAULT = Config()

# Крошечный сценарий для golden-теста (известный оптимум перебором/MILP)
SMALL = Config(N=3, M=2, G=2, K=20, Q=50, w_max=2, keep_alive=3)

# Cold-aware вариант DEFAULT (включён вес холодного старта)
COLD_AWARE = Config(delta_cs=0.3)
