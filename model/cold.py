"""
realized_cold — within-slot учёт холодного старта (закрывает слабость #4).

Правило: первый вызов функции типа g на узле j в данном слоте — холодный (если g
не была тёплой на НАЧАЛО слота); последующие вызовы того же типа на том же узле
в этом же слоте — уже тёплые (контейнер поднят первым вызовом). Это устраняет
завышение cold-start rate, которое возникало при «все одновременные вызовы холодные».

Используется ТОЛЬКО в реалистичной оценке (отчётные метрики). PSO-суррогат
оставляет более простой по-задачный учёт (быстрый, векторизуемый) — это
осознанное разделение «оптимизационный суррогат ≠ измеряемая метрика».
"""
import numpy as np


def realized_cold(assignment, tasks, warm_sets):
    """
    assignment[i] = j или -1. warm_sets = {j: set(тёплых типов на начало слота)}.
    Возвращает cold[n] ∈ {0,1}: 1 — задача реально оплатила холодный старт.
    """
    n = len(tasks)
    cold = np.zeros(n)
    started = set()                       # (j, g), уже стартовавшие в этом слоте
    for i, j in enumerate(assignment):
        if j < 0:
            continue                      # локально холодного старта нет
        g = tasks[i].g
        if g in warm_sets[j] or (j, g) in started:
            cold[i] = 0.0                 # тёплая (или уже поднята в этом слоте)
        else:
            cold[i] = 1.0                 # первый холодный вызов этого типа на узле
        started.add((j, g))
    return cold
